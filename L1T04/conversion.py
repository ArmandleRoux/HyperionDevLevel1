# Declared diffirent types of variables
num1 = 99.23    # float
num2 = 23       # integer
num3 = 150      # integer
string1 = "100" # string
# Use casting to convert variables from one type to another
num1 = int(num1)       # float to integer
num2 = float(num2)     # integer to float
num3 = str(num3)       # integer to string
string1 = int(string1) # string to integer
# Print to console all the variables on a seperate line
print(num1)
print(num2)
print(num3)
print(string1)
